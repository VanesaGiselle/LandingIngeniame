	$(document).ready(function(){
	  // Inicializar Tooltip de bootstrap
	  $('[data-toggle="tooltip"]').tooltip();  
	  
	  // suavizar el scroll a los links en navbar + footer 
	  $(".navbar a, footer a[href='#myPage']").on('click', function(event) { 
		// verificar el valor de this.hash antes de sobreescribir el comportamiento por defecto
		if (this.hash !== "") { 
		  // prevenir que se dispare el evento click 
		  event.preventDefault(); 
		  // guardar hash
		  var hash = this.hash; 
		  // con el método animate() de la librería de jQuery suavizamos el scroll
		  // el valor opcional (900) es el número en milisegundos que le toma en llegar al area especificada 
		  $('html, body').animate({
			scrollTop: $(hash).offset().top
		  }, 900, function(){ 
			// Agregar el valor hash (#) en la URL cuando termine el scroll
			window.location.hash = hash;
		  });
		} 
	  });
	})
	</script>

	
	
	
